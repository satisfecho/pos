#!/usr/bin/env node
const { execSync } = require('child_process');
const fs = require('fs');
const path = require('path');

const outputDir = path.join(__dirname, '../src/environments');
const outputPath = path.join(outputDir, 'commit-hash.ts');
const frontRoot = path.join(__dirname, '..');

function readExistingBuildInfo(filePath) {
  try {
    const s = fs.readFileSync(filePath, 'utf8');
    const h = s.match(/export const commitHash = '([^']*)'/);
    const v = s.match(/export const version = '([^']*)'/);
    return {
      commitHash: h ? h[1] : null,
      version: v ? v[1] : null,
    };
  } catch {
    return { commitHash: null, version: null };
  }
}

/** Keep previous hash when Docker mounts only ./front (no .git) or git is unavailable. */
function isPlausibleGitShortHash(h) {
  if (!h || h === 'git hash') return false;
  return /^[a-f0-9]{7,40}$/i.test(h);
}

if (!fs.existsSync(outputDir)) {
  fs.mkdirSync(outputDir, { recursive: true });
}

let version = '0.0.0';
try {
  const pkgPath = path.join(frontRoot, 'package.json');
  const pkg = JSON.parse(fs.readFileSync(pkgPath, 'utf8'));
  version = (pkg.version || version).replace(/['\\]/g, '');
} catch (e) {
  console.log('⚠ Could not read version from package.json, using 0.0.0');
}

let commitHash = 'git hash';

if (process.env.COMMIT_HASH && process.env.COMMIT_HASH.trim() !== '') {
  commitHash = process.env.COMMIT_HASH.trim().replace(/['\\]/g, '');
  console.log(`✓ Using commit hash from build argument: ${commitHash}`);
} else {
  try {
    const gitCommand = 'git rev-parse --short HEAD';
    commitHash = execSync(gitCommand, {
      encoding: 'utf-8',
      cwd: frontRoot,
      stdio: ['ignore', 'pipe', 'ignore'],
    })
      .trim()
      .replace(/['\\]/g, '');

    if (!commitHash || commitHash.length === 0) {
      throw new Error('Empty commit hash');
    }

    console.log(`✓ Commit hash generated from git: ${commitHash}`);
  } catch (error) {
    const existing = readExistingBuildInfo(outputPath);
    if (isPlausibleGitShortHash(existing.commitHash)) {
      commitHash = existing.commitHash;
      console.log(`✓ Kept existing commit hash (git unavailable): ${commitHash}`);
    } else {
      console.log('⚠ Could not get commit hash from git, using "git hash"');
      if (error.message) {
        console.log(`  Reason: ${error.message}`);
      }
    }
  }
}

const content = `// This file is auto-generated during build
// Do not edit this file manually
export const commitHash = '${commitHash}';
export const version = '${version}';
`;

try {
  fs.writeFileSync(outputPath, content, 'utf-8');
  console.log(`✓ Build info written: version=${version}, commitHash=${commitHash} -> ${outputPath}`);
} catch (writeError) {
  console.error(`✗ Error writing commit hash file: ${writeError.message}`);
  process.exit(0);
}
